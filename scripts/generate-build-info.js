/**
 * Generates src/generated/build-info.ts with git SHA and commit date.
 *
 * Works in two scenarios:
 *   1. Local machine — reads from git directly
 *   2. Vercel build — no .git, falls back to VERCEL_GIT_COMMIT_SHA env var
 *      (commit date unavailable on Vercel, so we use build time)
 */
const { execSync } = require("child_process");
const fs = require("fs");
const path = require("path");

function git(cmd) {
  try {
    const result = execSync(`git ${cmd}`, { encoding: "utf-8" }).trim();
    return result || null;
  } catch {
    return null;
  }
}

const sha =
  git("rev-parse --short HEAD") ||
  (process.env.VERCEL_GIT_COMMIT_SHA || "").slice(0, 7) ||
  "dev";

const commitDate =
  git("log -1 --format=%cI") ||
  new Date().toISOString();

const outDir = path.join(__dirname, "..", "src", "generated");
fs.mkdirSync(outDir, { recursive: true });

const content = `// Auto-generated by scripts/generate-build-info.js — do not edit
export const BUILD_VERSION = "${sha}";
export const BUILD_TIME = "${commitDate}";
`;

fs.writeFileSync(path.join(outDir, "build-info.ts"), content);
console.log(`[build-info] ${sha} @ ${commitDate}`);
